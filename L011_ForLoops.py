# 1 For Loop for String
for letter in "Harmony":  # Must be iterable
    print(letter)  # Print every letter

# 1A- Array
months = ["feb", "Jan"]
for i in range(len(months)):  # For array, I need number,
    print(months[i])  # To print for each item

# 2 For loop for number
for number in range(10):
    print(number)  # Print 10 numbers starting from 0...9
