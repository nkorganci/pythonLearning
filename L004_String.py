# 2b String functions
nameUpper = "nameUpper"
print(nameUpper.upper())  # single function
print(nameUpper.upper().isupper())  # double function
print(len(nameUpper))  # length
print(nameUpper[0])  # first character
print(nameUpper.index("n"))
print(nameUpper.index("m", 1, 4))
print(nameUpper.replace("n", "N"))